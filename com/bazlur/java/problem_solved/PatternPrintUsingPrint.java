package com.bazlur.java.problem_solved;

public class PatternPrintUsingPrint {
    public static void main(String[] args) {
        System.out.println("Right Half Pyramid");
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");

        System.out.println("Reverse Right Half Pyramid");
        System.out.println("* * * * *");
        System.out.println("* * * *");
        System.out.println("* * *");
        System.out.println("* *");
        System.out.println("*");

        System.out.println("Left Half Pyramid");
        System.out.println("        *");
        System.out.println("      * *");
        System.out.println("    * * *");
        System.out.println("  * * * *");
        System.out.println("* * * * *");

        System.out.println("Reverse Left Half Pyramid");
        System.out.println("* * * * *");
        System.out.println("  * * * *");
        System.out.println("    * * *");
        System.out.println("      * *");
        System.out.println("        *");

    }
}
