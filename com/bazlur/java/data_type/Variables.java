package com.bazlur.java.data_type;

public class Variables {
    public static void main(String[] args) {

        // Primitive data type & Non-primitive data type
        // Primitive data type:
        // int, double, float, char, boolean, byte, short, long
        // Non-primitive data type:
        // String, Array, Class, Interface, Exception,
        // Thread, Method, Constructor, Enum, Annotation

        // There two kinds of a Primitive data type:
        // 1. Numeric data type:
        // Integer, Double, Float, Character, Byte, Short, Long
        // 2. Boolean data type:
        //  boolean

        // type of Numeric data type:
        // 1. Integer: int, byte, short, long
        // 2. Double: double, float
        // 3. Character: char

        int age = 27;
        double salary = 100000.00;
        char gender = 'M';
        boolean isMarried = true;
        byte id = 127;
        short marks = 100;
        long phoneNumber = 1755684137L;

        System.out.println("Age : " + age);
        System.out.println("Salary : " + salary);
        System.out.println("Gender : " + gender);
        System.out.println("Is Married : " + isMarried);
        System.out.println("Id : " + id);
        System.out.println("Marks : " + marks);
        System.out.println("Phone Number : " + phoneNumber);



        System.out.println("Hello World");
    }

}
